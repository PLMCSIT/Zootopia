                                    if (isneg)
                                    {
                                        input_double *= -1;
                                    }
                                    item.setValue(input_double.ToString());
                                }
                                else
                                {
                                    runtime_error = true;
                                    Console.WriteLine("");
                                    Console.WriteLine("Runtime Error: Type Mismatch");
                                }
                                break;
                            case "Bull":
                                if (ins == "true" || ins == "false")
                                {
                                    item.setValue(ins);
                                }
                                else
                                {
                                    runtime_error = true;
